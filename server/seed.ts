export async function seedDatabase() {
  const existing = await db.select().from(users).where(eq(users.username, "demo"));
  if (existing.length > 0) {
    const existingAssignments = await storage.getAssignments(existing[0].id);
    if (existingAssignments.length > 0) {
      return;
    }
    await seedAssignments(existing[0].id);
    return;
  }

  const user = await storage.createUser({
    username: "demo",
    password: "demo123",
    fullName: "Solace",
    email: "solace@student.edu",
    schoolAffiliation: "Pasadena High School",
  });

  await storage.updateUser(user.id, {
    canvasBaseUrl: "https://pasadena.instructure.com",
    canvasConnected: false,
  });

  await seedAssignments(user.id);
}
